#include <iostream>
#include <cctype>
#include <cmath>
#include <string>
#include <cstring>

using namespace std;



int main (){

    int n;
    cin >> n;
    int m = n*2;
    int A[n][m] , B[m][n];

    for (int i = 0; i <n; ++i) {
        for (int j = 0; j < m ; ++j) {
            cin >> A[i][j];
        }
    }

    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            B[i][j] = A[i][j];
        }
    }

    for (int i = 0; i < n; ++i) {
        for (int j = n; j <m ; ++j) {
            B[i+n][j-n] = A[i][j];
        }
    }

    for (int i = 0; i < m; ++i) {
        for (int j = 0; j < n; ++j) {
            cout << B[i][j] << " ";
        }
        cout<<endl;
    }


    return 0;
}

